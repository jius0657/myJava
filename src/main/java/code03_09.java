public class code03_09 {
    public static void main(String[] args) {
        int num = 20;
        num++ ; System.out.print(num+" ");
        //++ = 값 1 증가
        num-- ; System.out.print(num+" ");
        //-- = 값 1 감소
        num += 3 ; System.out.print(num+" ");
        // += 3 = num + 3과 동일
        num -= 3 ; System.out.print(num+" ");
        // -= 3 = num - 3과 동일
        num *= 3 ; System.out.print(num+" ");
        // *= 3 = num * 3과 동일
        num /= 3 ; System.out.print(num+" ");
        // /= 3 = num / 3과 동일
        num %= 3 ; System.out.print(num+" ");
        // %= 3 = num % 3과 동일

    }
}
