public class code04_03 {
    public static void main(String[] args) {

        //변수 선언
        int n1 = 1; //첫번쨰 정수
        int n2 = 2; //두번쨰 정수

        System.out.println(n1+n2); //(1 + 2)
        System.out.println(n1-n2); //(1 - 2)
        System.out.println(n1*n2); //(1 * 2)
        System.out.println(n1/n2); //(1 / 2)

    }
}
