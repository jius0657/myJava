public class Code04_04 {
    public static void main(String[] args) {
        boolean boo1, boo2; //boo1, boo2 두 개의 변수를 만듬
        //boolean은 참(true) 또는 거짓(false)만 저장하는 자료형

        boo1 = true; //true 값을 저장
        System.out.println(boo1); //저장한 값 출력

        boo2 = (100 < 10); //100이 10보다 작은지 비교하여 결과 저장
        System.out.println(boo2); //저장한 값 출력
    }
}
